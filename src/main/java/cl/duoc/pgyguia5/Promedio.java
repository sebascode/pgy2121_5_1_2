    private void btnCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalcularActionPerformed
        // TODO add your handling code here:
        try{
            int nota1 = Integer.parseInt(txtNota1.getText());
            int nota2 = Integer.parseInt(txtNota2.getText());
            int nota3 = Integer.parseInt(txtNota3.getText());
            
            int porc1 = Integer.parseInt(txtPorc1.getText());
            int porc2 = Integer.parseInt(txtPorc2.getText());
            int porc3 = Integer.parseInt(txtPorc3.getText());
            
            if((porc1 + porc2 + porc3) != 100){
                throw new Exception("Promedio total no da 100%");
            }
            if(nota1 > 70 || nota2 > 70 || nota3 > 70){
                throw new Exception("Nota 1, 2 o 3 sobre 70.");
            }
            if(nota1 < 0 || nota2 < 0 || nota3 < 0){
                throw new Exception("Nota 1, 2 o 3 negativa.");
            }
            
            // EJECUCIÃ“N
            final float f_nota1 = Float.parseFloat(nota1+"") * porc1 / 100;
            final float f_nota2 = Float.parseFloat(nota2+"") * porc2 / 100;
            final float f_nota3 = Float.parseFloat(nota3+"") * porc3 / 100;
            final float nota = f_nota1 + f_nota2 + f_nota3;
            
            lblPromedio.setText(nota+"");
        } catch(Exception ex){
            System.out.println(ex.getMessage());
        }
    }//GEN-LAST:event_btnCalcularActionPerformed

